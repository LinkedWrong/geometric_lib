def area(a):
    """Calculates the area of a square.

    Args:
        a: The length of one side of the square.

    Returns:
        The area of the square.
    """
    return a * a


def perimeter(a):
    """Calculates the perimeter of a square.

    Args:
        a: The length of one side of the square.

    Returns:
        The perimeter of the square.
    """
    return 4 * a
