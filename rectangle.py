def area(a, b):
    """Calculates the area of a rectangle.

    Args:
        a: The length of the rectangle.
        b: The width of the rectangle.

    Returns:
        The area of the rectangle.
    """
    return a * b

def perimeter(a, b):
    """Calculates the perimeter of a rectangle.

    Args:
        a: The length of the rectangle.
        b: The width of the rectangle.

    Returns:
        The perimeter of the rectangle.
    """
    return (a + b) * 2
